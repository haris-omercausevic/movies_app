import 'package:meta/meta.dart';

class TranslationModel {
  final String home;
  final String username;
  final String password;
  final String login;
  final String logout;
  final String facilities;
  final String events;
  final String messages;
  final String me;
  final String noContent;
  final String signIn;
  final String signUp;
  final String settings;
  final String firstName;
  final String lastName;
  final String birthDate;
  final String gender;
  final String city;
  final String email;
  final String invitations;
  final String publicAttendance;
  final String publicInvitations;
  final String publicFriendships;
  final String publicFriendshipsForFriends;
  final String showMessageContentInNotification;
  final String myLocations;
  final String friends;
  final String myEvents;
  final String selectLocation;
  final String close;
  final String inbox;
  final String outbox;

  final String loadPopularMovies;
  final String loadComedyMovies;

  const TranslationModel({
    @required this.home,
    @required this.username,
    @required this.password,
    @required this.login,
    @required this.logout,
    @required this.facilities,
    @required this.events,
    @required this.messages,
    @required this.me,
    @required this.noContent,
    @required this.signIn,
