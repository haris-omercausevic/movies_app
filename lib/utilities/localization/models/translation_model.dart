    @required this.invitations,
    @required this.publicAttendance,
    @required this.publicInvitations,
    @required this.publicFriendships,
    @required this.publicFriendshipsForFriends,
    @required this.showMessageContentInNotification,
    @required this.myLocations,
    @required this.friends,
    @required this.myEvents,
    @required this.selectLocation,
    @required this.close,
    @required this.inbox,
    @required this.outbox,

