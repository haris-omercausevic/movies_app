        assert(username != null),
        assert(password != null),
        assert(login != null),
        assert(logout != null),
        assert(facilities != null),
        assert(events != null),
        assert(messages != null),
        assert(me != null),
        assert(noContent != null),
        assert(signIn != null),
        assert(signUp != null),
        assert(settings != null),
        assert(firstName != null),
        assert(lastName != null),
        assert(birthDate != null),
        assert(gender != null),
        assert(city != null),
