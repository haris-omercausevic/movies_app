        assert(invitations != null),
        assert(publicAttendance != null),
        assert(publicInvitations != null),
        assert(publicFriendships != null),
        assert(publicFriendshipsForFriends != null),
        assert(showMessageContentInNotification != null),
        assert(myLocations != null),
        assert(friends != null),
        assert(myEvents != null),
        assert(close != null),
        assert(selectLocation != null),
        assert(inbox != null),
        assert(outbox != null),
        
        assert(loadPopularMovies != null),
        assert(loadComedyMovies != null);
